from __future__ import annotations

"""Support-AI templates.

Требования:
- обращение строго на «Вы»;
- без давления, без «продажи в лоб»;
- создаём ощущение большого сопровождения (контейнирование + интерпретация + мягкий шаг).
"""


def render_pre(
    *,
    mode: str,
    area: str | None,
    same_area_days: int,
    avg_delta_7d: float | None,
    skip_count_5d: int,
) -> str:
    area_txt = f" в зоне: {area}" if area else ""

    # Универсальные «мягкие» фразы (любя)
    gentle_open = "Сейчас важно не стараться, а просто позволить системе сделать свою часть."\
        " Вам не нужно делать это ‘правильно’."

    if mode == "reentry":
        return (
            "Я вижу паузу в ритме — и это нормально.\n\n"
            "После перерыва тело часто реагирует сильнее не потому, что ‘стало хуже’, "
            "а потому что исчезла привычная последовательность.\n\n"
            f"Если Вы замечаете напряжение{area_txt}, это может быть просто сигналом: ‘мне нужна опора’.\n\n"
            f"{gentle_open}\n\n"
            "Поэтому сегодня Вам присылается следующее аудио: мягкий вход и восстановление ритма."
        )

    if mode == "accumulation":
        streak = "" if same_area_days < 3 else f" уже {same_area_days} дня подряд"
        return (
            f"Я вижу, что Вы{streak} отмечаете напряжение{area_txt}.\n\n"
            "Когда одна зона держится несколько дней, это чаще говорит не о разовом всплеске, "
            "а о фоновом удержании — будто система всё время остаётся ‘на чеку’.\n\n"
            "И Вы в этом не виноваты: так тело обычно защищает устойчивость, когда вокруг много задач или неопределённости.\n\n"
            "В такой фазе лучше не ‘дожимать расслабление’. Сильное усилие иногда только усиливает контроль.\n\n"
            f"{gentle_open}\n\n"
            "Поэтому сегодня Вам присылается следующее аудио: мягкое снижение фонового контроля."
        )

    if mode == "destabilization":
        extra = ""
        if avg_delta_7d is not None:
            extra = f"\n\nЗа последние дни средняя динамика: {avg_delta_7d:+.1f}. Это похоже на фазу перестройки, а не на ‘поломку’."
        return (
            "Я вижу, что состояние идёт волнами: иногда легче, иногда снова собирается.\n\n"
            "Так бывает, когда контроль начинает ослабевать, но система ещё проверяет безопасность изменений. "
            "Это не откат и не ошибка.\n\n"
            "В такие дни важнее не усилить воздействие, а дать телу ощущение опоры."
            f"{extra}\n\n"
            f"{gentle_open}\n\n"
            "Поэтому сегодня Вам присылается следующее аудио: стабилизация и опора."
        )

    # release / baseline
    base = (
        "Я фиксирую Ваше состояние сейчас.\n\n"
        "Если сегодня будет даже маленький сдвиг — это уже сигнал, что система умеет отпускать. "
        "Мы не торопим процесс и не требуем результата.\n\n"
        f"{gentle_open}\n\n"
    )

    if mode == "release":
        return base + "Поэтому сегодня Вам присылается следующее аудио: закрепление и мягкое отпускание."

    return base + "Поэтому сегодня Вам присылается следующее аудио: базовая поддержка ритма."


def render_no_support(*, kind_label: str = "") -> str:
    kind_label = (kind_label or "").strip()
    suffix = f" для ‘{kind_label}’" if kind_label else ""
    return (
        "Сейчас сопровождение не активно.\n\n"
        "Вы можете слушать аудио, но я не смогу подстроить ритм под Ваше состояние "
        "и дать персональную реакцию системы.\n\n"
        "Если Вы хотите вернуть эффект ‘меня ведут и поддерживают’, достаточно восстановить сопровождение" + suffix + "."
    )
